    private void CreateProfileButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CreateProfileButtonActionPerformed
        // TODO add your handling code here:
        String username = UsernameInput.getText();
        String password = new String(PasswordInput.getPassword());

        if (!isValidPassword(password)) {
            OutputMessage.setText("Password should be at least 8 characters long, contain at least one capital letter, one number, and one special character.");
            return;
        }

        try {
            String userData = username + "," + password + "\n";
            if (!userExists(username)) {
                BufferedWriter writer = new BufferedWriter(new FileWriter("src/Users", true));
                writer.append(userData);
                writer.close();
                OutputMessage.setText("User profile created successfully!");
            } else {
                OutputMessage.setText("Username already exists! Please choose a different one.");
            }
        } catch (IOException e) {
            OutputMessage.setText("Error occurred while creating the profile.");
        }
        
    }//GEN-LAST:event_CreateProfileButtonActionPerformed
     private boolean isValidPassword(String password) {
        String regex = "^(?=.*[0-9])(?=.*[a-z])(?=.*[A-Z])(?=.*[@#$%^&+=])(?=\\S+$).{8,}$";
        Pattern pattern = Pattern.compile(regex);
        Matcher matcher = pattern.matcher(password);
        return matcher.matches();
    }

    private boolean userExists(String username) throws IOException {
        return Files.lines(Paths.get("src/Users")).anyMatch(line -> line.startsWith(username + ","));
    }
